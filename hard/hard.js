function add(val1, val2) {
  let sum = val1 + val2;
  return sum;
}

function subtract(val1, val2) {
  let difference = val1 - val2;
  return difference;
}

function divide(val1, val2) {
  let qoutient = val1 / val2;
  return qoutient;
}

function multiply(val1, val2) {
  let product = val1 * val2;
  return product;
}
